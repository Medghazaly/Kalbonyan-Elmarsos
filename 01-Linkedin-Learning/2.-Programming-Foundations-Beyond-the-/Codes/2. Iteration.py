# Loop

spices = [
    'Pasta',
    'Salt',
    'pepper',
    'egg',
    'Water',
]

# for Codition
for spice in spices:
    print(spice)
print('No more boring Pasta!')


# while Condition

print('Counting to 100 by fives:')
i = 5
while i <= 100:
    print(i)
    i += 5
print('List complete!')

